namespace DerailedDeliveries.Framework.Audio
{
    /// <summary>
    /// Enum for the different types of UI audio.
    /// </summary>
    public enum AudioCollectionTypes
    {
        /// <summary>
        /// Sound type for UI back buttons.
        /// </summary>
        Back,

        /// <summary>
        /// Sound type for UI select buttons.
        /// </summary>
        Select,

        /// <summary>
        /// Sound type for UI select buttons.
        /// </summary>
        Denied,

        /// <summary>
        /// Sound type for in-game buttons.
        /// </summary>
        Button,

        /// <summary>
        /// Sound type for in-game switches.
        /// </summary>
        Switch,

        /// <summary>
        /// Sound type for showing popups.
        /// </summary>
        Popup,

        /// <summary>
        /// Sound type for successfuly delivering packages.
        /// </summary>
        SuccessfulDeliver,

        /// <summary>
        /// Sound type for repairing.
        /// </summary>
        Repair,

        /// <summary>
        /// Sound type for when a player joined the lobby.
        /// </summary>
        PlayerJoined,

        /// <summary>
        /// Sound type for when a player drops a grabbable.
        /// </summary>
        DropGrabbable,

        /// <summary>
        /// Sound type for when a player grabs a grabbable.
        /// </summary>
        GrabGrabbable,

        /// <summary>
        /// Sound type for when a player deposits coal in the coal oven.
        /// </summary>
        DepositCoal,

        /// <summary>
        /// Sound type for when a player turns the coal oven on.
        /// </summary>
        CoalOvenOn,

        /// <summary>
        /// Sound type for when a player turns the coal oven off.
        /// </summary>
        CoalOvenOff,    
    }
}
