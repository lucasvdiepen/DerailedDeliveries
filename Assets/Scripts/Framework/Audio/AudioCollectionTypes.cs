namespace DerailedDeliveries.Framework.Audio
{
    /// <summary>
    /// Enum for the different types of UI audio.
    /// </summary>
    public enum AudioCollectionTypes
    {
        /// <summary>
        /// Sound type for UI back buttons.
        /// </summary>
        Back,

        /// <summary>
        /// Sound type for UI select buttons.
        /// </summary>
        Select,

        /// <summary>
        /// Sound type for UI select buttons.
        /// </summary>
        Denied,

        /// <summary>
        /// Sound type for in-game buttons.
        /// </summary>
        Button,

        /// <summary>
        /// Sound type for in-game switches.
        /// </summary>
        Switch,

        /// <summary>
        /// Sound type for showing popups.
        /// </summary>
        Popup,
    }
}
